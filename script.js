// ============ LANGUAGE SYSTEM ============
let currentLang = localStorage.getItem('menassa-lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('menassa-lang', lang);

    // Set direction
    const isRTL = lang === 'ar';
    document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = ' ';
            }
            el.textContent = translations[lang][key];
        }
    });

    // Update placeholders for form labels
    document.querySelectorAll('.form-group label[data-i18n]').forEach(label => {
        const key = label.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            label.textContent = translations[lang][key];
        }
    });

    // Update active lang button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
}

// ============ NAVBAR ============
function initNavbar() {
    const navbar = document.getElementById('navbar');
    const mobileMenuBtn = document.getElementById('mobileMenuBtn');
    const navLinks = document.getElementById('navLinks');

    // Scroll effect - add background on scroll
    window.addEventListener('scroll', () => {
        navbar.classList.toggle('scrolled', window.scrollY > 50);
    });

    // Mobile menu
    mobileMenuBtn.addEventListener('click', () => {
        mobileMenuBtn.classList.toggle('active');
        navLinks.classList.toggle('active');
        document.body.style.overflow = navLinks.classList.contains('active') ? 'hidden' : '';
    });

    // Close menu on link click
    navLinks.querySelectorAll('.nav-link').forEach(link => {
        link.addEventListener('click', () => {
            mobileMenuBtn.classList.remove('active');
            navLinks.classList.remove('active');
            document.body.style.overflow = '';
        });
    });

    // Active link on scroll
    const sections = document.querySelectorAll('section[id]');
    window.addEventListener('scroll', () => {
        const scrollPos = window.scrollY + 200;
        sections.forEach(section => {
            const top = section.offsetTop;
            const height = section.offsetHeight;
            const id = section.getAttribute('id');
            const link = document.querySelector(`.nav-link[href="#${id}"]`);
            if (link) {
                if (scrollPos >= top && scrollPos < top + height) {
                    document.querySelectorAll('.nav-link').forEach(l => l.classList.remove('active'));
                    link.classList.add('active');
                }
            }
        });
    });
}

// ============ CURSOR GLOW ============
function initCursorGlow() {
    const glow = document.getElementById('cursorGlow');
    if (!glow || window.innerWidth < 768) return;

    document.addEventListener('mousemove', (e) => {
        glow.style.left = e.clientX + 'px';
        glow.style.top = e.clientY + 'px';
    });
}

// ============ SCROLL REVEAL ============
function initScrollReveal() {
    const reveals = document.querySelectorAll('.reveal-up');

    // Immediately reveal elements in the hero section with stagger
    const heroReveals = document.querySelectorAll('.hero .reveal-up');
    heroReveals.forEach((el, i) => {
        setTimeout(() => {
            el.classList.add('revealed');
        }, 200 + i * 150);
    });

    // Also reveal hero-visual
    const heroVisual = document.querySelector('.hero-visual.reveal-up');
    if (heroVisual) {
        setTimeout(() => {
            heroVisual.classList.add('revealed');
        }, 200 + heroReveals.length * 150);
    }

    // Observer for remaining elements
    const observer = new IntersectionObserver((entries) => {
        const visible = entries.filter(e => e.isIntersecting);
        visible.forEach((entry, index) => {
            setTimeout(() => {
                entry.target.classList.add('revealed');
            }, index * 120);
            observer.unobserve(entry.target);
        });
    }, {
        threshold: 0.05,
        rootMargin: '50px 0px -20px 0px'
    });

    reveals.forEach(el => {
        if (!el.classList.contains('revealed')) {
            observer.observe(el);
        }
    });
}

// ============ COUNTER ANIMATION ============
function initCounters() {
    const counters = document.querySelectorAll('.stat-number[data-count]');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const el = entry.target;
                const target = parseInt(el.getAttribute('data-count'));
                const duration = 2000;
                const start = performance.now();

                function update(now) {
                    const elapsed = now - start;
                    const progress = Math.min(elapsed / duration, 1);
                    // Ease out cubic
                    const eased = 1 - Math.pow(1 - progress, 3);
                    el.textContent = Math.floor(eased * target);

                    if (progress < 1) {
                        requestAnimationFrame(update);
                    } else {
                        el.textContent = target;
                    }
                }

                requestAnimationFrame(update);
                observer.unobserve(el);
            }
        });
    }, { threshold: 0.5 });

    counters.forEach(counter => observer.observe(counter));
}

// ============ SMOOTH SCROLL ============
function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
}

// ============ CONTACT FORM ============
function initContactForm() {
    const form = document.getElementById('contactForm');
    if (!form) return;

    form.addEventListener('submit', (e) => {
        e.preventDefault();

        const btn = form.querySelector('button[type="submit"]');
        const originalText = btn.querySelector('span').textContent;

        btn.querySelector('span').textContent = currentLang === 'ar' ? '\u062C\u0627\u0631\u064A \u0627\u0644\u0625\u0631\u0633\u0627\u0644...' : currentLang === 'fr' ? 'Envoi en cours...' : 'Sending...';
        btn.disabled = true;

        setTimeout(() => {
            btn.querySelector('span').textContent = currentLang === 'ar' ? '\u062A\u0645 \u0627\u0644\u0625\u0631\u0633\u0627\u0644!' : currentLang === 'fr' ? 'Envoy\u00E9 !' : 'Sent!';
            btn.style.background = '#10B981';

            setTimeout(() => {
                form.reset();
                btn.querySelector('span').textContent = originalText;
                btn.disabled = false;
                btn.style.background = '';
            }, 2000);
        }, 1500);
    });
}

// ============ CHART BAR ANIMATION ============
function initChartAnimation() {
    const chart = document.querySelector('.mockup-chart');
    if (!chart) return;

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const bars = chart.querySelectorAll('.chart-bar');
                bars.forEach((bar, i) => {
                    bar.style.animationDelay = `${i * 0.15}s`;
                });
                observer.unobserve(chart);
            }
        });
    }, { threshold: 0.3 });

    observer.observe(chart);
}

// ============ PARALLAX EFFECT ============
function initParallax() {
    const orbs = document.querySelectorAll('.floating-orb');

    window.addEventListener('scroll', () => {
        const scrollY = window.scrollY;
        orbs.forEach((orb, i) => {
            const speed = (i + 1) * 0.1;
            orb.style.transform = `translateY(${scrollY * speed}px)`;
        });
    });
}

// ============ TILT EFFECT ON CARDS ============
function initTiltEffect() {
    if (window.innerWidth < 768) return;

    const cards = document.querySelectorAll('.service-card, .about-card, .testimonial-card');

    cards.forEach(card => {
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            const rotateX = (y - centerY) / 20;
            const rotateY = (centerX - x) / 20;

            card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateY(-4px)`;
        });

        card.addEventListener('mouseleave', () => {
            card.style.transform = '';
        });
    });
}

